package com.company;

public class BasicsLoops {
    public static void main(String[] args){
        if(true){
            System.out.println("Hello World");
        }

        int count = 1;
        while(count!=5){
            System.out.println(count);
            count ++ ;
        }

        //For loops
        for(int forCount=0;forCount!=5;forCount++){
            System.out.println(forCount);
        }

    }
}
